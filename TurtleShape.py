 	turtle.pencolor("pink")
 	turtle.forward(250)
 	turtle.right(144)
 	turtle.pencolor("white")
 	turtle.forward(250)
 	turtle.right(144)
 	turtle.pencolor("red")
 	turtle.forward(250)
 	turtle.right(144)
 	turtle.pencolor("yellow")
